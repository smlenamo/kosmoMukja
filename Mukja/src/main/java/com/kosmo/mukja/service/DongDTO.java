package com.kosmo.mukja.service;

public class DongDTO {
	private String code;
	private String sido;
	private String si_goon_go;
	private String dong;
	private String dong_sub;
	private String lat;
	private String lng;
	
	public DongDTO() {}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getSido() {
		return sido;
	}

	public void setSido(String sido) {
		this.sido = sido;
	}

	public String getSi_goon_go() {
		return si_goon_go;
	}

	public void setSi_goon_go(String si_goon_go) {
		this.si_goon_go = si_goon_go;
	}

	public String getDong() {
		return dong;
	}

	public void setDong(String dong) {
		this.dong = dong;
	}

	public String getDong_sub() {
		return dong_sub;
	}

	public void setDong_sub(String dong_sub) {
		this.dong_sub = dong_sub;
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLng() {
		return lng;
	}

	public void setLng(String lng) {
		this.lng = lng;
	}
	
	
	
	
}
